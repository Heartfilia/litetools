var localCache types.ConfigJson
var localCacheMu sync.RWMutex

func fetchRemoteConfig(url string) string {
	client := &http.Client{Timeout: 5 * time.Second}
	resp, err := client.Get(url)
	if err != nil {
		return ""
	}
	defer func() {
		_ = resp.Body.Close()
	}()
	if resp.StatusCode != http.StatusOK {
		return ""
	}
	item, err := io.ReadAll(resp.Body)
	if err != nil {
		return ""
	}
	return string(item)
}

func sureLocal(configJson string) bool {
	// 确保本地有缓存记录
	if litedir.FileExists(configJson) {
		return true //存在返回存在的状态
	}
	// 不存在这里下载
	// 缓存在这里 后续直接用
	// https://googlechromelabs.github.io/chrome-for-testing/known-good-versions.json
	requestJson := fetchRemoteConfig("https://static.litetools.top/source/json/useragent.json")
	if requestJson == "" {
		// 这里下载失败 直接返回false
		return false
	}
	if !litedir.FileSaver(requestJson, configJson) {
		// 这里下载失败 直接返回false
		return false
	}
	return true
}

func readFromLocal(configJson string) types.ConfigJson {
	file, err := os.ReadFile(configJson)
	if err != nil {
		return types.ConfigJson{}
	}
	var res types.ConfigJson
	err = json.Unmarshal(file, &res)
	if err != nil {
		return types.ConfigJson{}
	}
	return res
}

func configFromCache() types.ConfigJson {
	localCacheMu.RLock()
	if !localCache.IsEmpty() {
		// 直接返回缓存好的数据 避免重复加载io
		cache := localCache
		localCacheMu.RUnlock()
		return cache
	}
	localCacheMu.RUnlock()

	localCacheMu.Lock()
	defer localCacheMu.Unlock()
	if !localCache.IsEmpty() {
		return localCache
	}
	baseDir := litedir.LiteDir()
	browserDir := path.Join(baseDir, "browser")
	if !litedir.FileExists(browserDir) {
		_ = os.MkdirAll(browserDir, 0o777)
	}
	configJson := path.Join(browserDir, "config.json")
	local := sureLocal(configJson)
	if local {
		// 本地有 然后也做对应的处理
		res := readFromLocal(configJson)
		if !res.IsEmpty() {
			localCache = res
			return res
		}
	}
	return defaultSetting
}

